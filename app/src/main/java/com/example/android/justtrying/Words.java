package com.example.android.justtrying;

public class Words {


    public String khasi, english;

    public Words(){

    }

    public Words(String khasi, String english) {
        this.khasi = khasi;
        this.english = english;
    }
    public String getKhasi() {
        return khasi;
    }

    public String getEnglish() {
        return english;
    }

    public void setKhasi(String khasi) {
        this.khasi = khasi;
    }

    public void setEnglish(String english) {
        this.english = english;
    }
}
